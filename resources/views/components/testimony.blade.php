                                            My name is Godwin Makokha. I am the center manager for an NGO called CAP youth empowerment institute Athi River center. It’s a registered NGO which offers basic employability skills training to youths for a period of 3 months and then links them to placement opportunities. We offer demand driven in technical skills, life skills, entrepreneurship skills and basic IT skills.
                                            The founder of DREAMERS CLUB AFRICA, Mr. Andrew was introduced to me by a friend in January 2019 and he was so passionate with what our organization does for the youths. He volunteered not only to mentor these youths from poor background but also teaching money and entrepreneurship skills. The key topics were savings, idea generation and business plan implementation through communication and presentation skills.
                                            His style of approach is very different from what I have seen. He says, ‘’if you want anyone to listen to you, bring yourself to their level, listen keenly to them, understand them and then speak or guide them’’. This made our trainees appreciate him as our best mentor. Secondly he spent good time taking our trainees through psychology lessons to shift their mind set, as  education system have taught us to think in a structured way e.g. ‘A’ is for apple but it  can also be ‘alphabet, admission, antelope’, and so on. He structures his classes to be collaborative and not competitive, team work driven and very engaging. He encourages mistakes as a way of learning and I watched our students come up with all sorts of suggestions or answers while he guided them. I realized the former teaches ‘what’ to think, the latter teaches ‘how’ to think.
                                            We had also two entrepreneurship completion at CAP among centers within Nairobi Region. Out of the two we scoped position 1 which was something our center had never managed. The business ideas were so unique and solving social real problem e.g. an app to improve network at rural areas, an app to reduce road accidents by detecting seat belts that were off, bio fuel among other concepts. Our students left with a very different mindset and ready to tackle the economic hardship. We are very grateful to have such an amazing mentor who deeply cares for the next generation.

